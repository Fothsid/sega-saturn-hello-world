#include "gfx.h"

// 64K of VRAM for commands... that should be way more than enough haha
#define VDP1_MAX_CMD_COUNT (2048)

// Address in the VDP1 memory to where the font will be located...
#define FONT_VDP1_ADDR (VDP1_VRAM + VDP1_MAX_CMD_COUNT*32)

u8 font[];

void gfx_init()
{
    vdp2_init();
    vdp1_init();
}

void gfx_load_font()
{
    for (int i = 0; i < 96; i++)
    {
        u8* addr = FONT_VDP1_ADDR + (64 * i);
        u8* src = font + (8 * i);
        u8 bit = 0x80;
        for (int x = 0; x < 64; x++)
        {
            addr[x] = (src[x / 8] & bit) ? 1 : 0;
            bit >>= 1;
            if (bit == 0) bit = 0x80;
        }
    }
}

void gfx_print(const char* str, int x, int y)
{
    int cx = x, cy = y;
    while (*str)
    {
        if (*str == '\n')
        {
            cx = x;
            cy += 8;
        }
        else
        {
            vdp1_draw_sprite(cx, cy, 8, 8, FONT_VDP1_ADDR + (64 * (*str - 0x20)), PMOD_DEFAULT);
        	cx += 8;
        }
        str++;
    }
}

